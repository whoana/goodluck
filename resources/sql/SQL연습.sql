	 substr(a.REG_DATE,1, 8)  AS "date"
	,count(a.INTERFACE_ID)    AS "cnt"
  FROM tan0201 a
 WHERE a.REG_DATE >= '20220101' || '000000000'
GROUP BY substr(a.REG_DATE,1, 8) , a.MOD_USER
ORDER BY 1 DESC
